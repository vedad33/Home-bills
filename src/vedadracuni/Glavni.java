    //Vedad Unos Struje
    public void Struja() {

        Thread thread = new Thread() {
            @Override
            public void run() {

                if (state) {

                    Session session = HibernateUtil.getSessionFactory().openSession();
                    String cijenastruje = CenaStruja.getText();
                    if (cijenastruje.equals("Cijena struje...") || cijenastruje.matches("^[a-zA-Z]+$")) {

                        JOptionPane.showMessageDialog(null, "Unesite cijenu STRUJE(SAMO BROJEVI)!");

                    } else {
                        double struja = Double.parseDouble(cijenastruje);

                        Object StrujaMjesecGodina = MjesecGodina.getSelectedItem();
                        String strujaMjesecGodina = String.valueOf(StrujaMjesecGodina);
                        Vedad vedad = new Vedad();
                        vedad.setDatum(strujaMjesecGodina);
                        vedad.setStruja(struja);

                        try {

                            transaction = session.beginTransaction();
                            session.save(vedad);
                            transaction.commit();
                            JOptionPane.showMessageDialog(null, "Unos uspjesan!");

                        } catch (HibernateException exception) {
                            if (transaction != null) {

                                transaction.rollback();
                            }

                            JOptionPane.showMessageDialog(null, exception.getMessage());
                        } finally {

                            if (session != null) {

                                session.close();
                            }
                        }
                    }
                }
            }

        };
        thread.start();

    }

    //Vedad Ukupno rezija
    public void UkupnoRezija() {

        Thread thread = new Thread() {
            @Override
            public void run() {

                if (state) {

                    Session session = HibernateUtil.getSessionFactory().openSession();

                    Object UKUPNO = UkupniTroskovi.getSelectedItem();
                    String Ukupnitroskovi = String.valueOf(UKUPNO);
                    String upit = " select struja + oki + komunalije + telemach + plin + voda + kreditna_kartica  from Vedad where datum='" + Ukupnitroskovi + "'";
                    Object rezultat = 0;

                    Query query = session.createQuery(upit);

                    try {

                        transaction = session.beginTransaction();

                        try {
                            rezultat = query.list().get(0);
                        } catch (IndexOutOfBoundsException exception) {
                            JOptionPane.showMessageDialog(null, "Nema Rezultata!");

                        }

                        transaction.commit();

                    } catch (HibernateException exception) {
                        if (transaction != null) {

                            transaction.rollback();
                        }

                        JOptionPane.showMessageDialog(null, exception.getMessage());
                    }

                    String ukupno = String.valueOf(rezultat);
                    JOptionPane.showMessageDialog(null, "Ukupno Rezija  :" + "\n" + Ukupnitroskovi + "\n" + ukupno + " KM");
                    String upitUkupno = "update  Vedad set ukupno = '" + ukupno + "' " + " where datum ='" + Ukupnitroskovi + "'";
                    Query query1 = session.createQuery(upitUkupno);

                    try {

                        transaction = session.beginTransaction();
                        query1.executeUpdate();
                        session.flush();
                        session.clear();
                        transaction.commit();
                    } catch (HibernateException exception) {

                        if (transaction != null) {

                            transaction.rollback();
                        }

                    } finally {

                        if (session != null) {

                            session.close();
                        }
                    }
                }
            }
        };
        thread.start();
    }

